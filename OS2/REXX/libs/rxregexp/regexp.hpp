#ifndef REGEXP
#define REGEXP

// bits 31..28 : operator code
// bits 27..16 : set number
// bits 15.. 0 : character
#define SCAN    0xf0000000     // scan delimiter / mask for operator code
#define EPSILON 0x10000000     // epsilon transition
#define NOT     0x20000000     // invert symbol (^)
#define ANY     0x40000000     // any symbol (?)
#define SET     0x80000000     // set ([...])
#define EOP     0x00000000     // end of pattern state

// error definitions
enum RE_ERROR { E_UNEXPECTED_SYMBOL = 1, E_MISSING_PAREN_CLOSE, E_ILLEGAL_SET,
                E_UNEXPECTED_EOP, E_ILLEGAL_NUMBER, E_ILLEGAL_SYMBOLIC_NAME };

#endif
